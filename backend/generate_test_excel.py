def generate_excel_files():
    """모든 테스트 파일 생성"""
    print("🚀 테스트용 엑셀 파일 생성 시작...\n")
    
    for key, dataset in datasets.items():
        # DataFrame 생성
        df = pd.DataFrame({'text': dataset['data']})
        
        # Excel 파일 저장
        excel_filename = f"test_{key}.xlsx"
        df.to_excel(excel_filename, index=False, engine='openpyxl')
        print(f"✅ {excel_filename} 생성 완료 - {dataset['name']}")
        
        # CSV 파일도 저장
        csv_filename = f"test_{key}.csv"
        df.to_csv(csv_filename, index=False, encoding='utf-8-sig')
        print(f"✅ {csv_filename} 생성 완료\n")
    
    print(f"\n🎉 총 {len(datasets) * 2}개 파일 생성 완료!")
    print("\n📋 생성된 파일 목록:")
    for key, dataset in datasets.items():
        print(f"  - test_{key}.xlsx ({len(dataset['data'])}개 항목)")
        print(f"  - test_{key}.csv ({len(dataset['data'])}개 항목)")
    
    print("\n💡 사용 방법:")
    print("  1. 원하는 파일을 번역 앱에 업로드하세요")
    print("  2. 텍스트 컬럼명: 'text'")
    print("  3. 소스 언어: 한국어 (ko)")
    print("  4. 타겟 언어: 영어 (en) 또는 원하는 언어")
    print("\n⚡ 추천: 먼저 test_basic_10.xlsx로 테스트해보세요!")

if __name__ == "__main__":
    generate_excel_files()